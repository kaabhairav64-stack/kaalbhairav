      <span class="source-tag">Source: Skanda Purana — Kashi Khanda, Chapter 31</span>
      <h3 class="story-title">Bhairav Becomes Kotwal of Kashi</h3>
      <p>
        After being absolved of his sin at Kashi, Kaal Bhairav stood before Lord Shiva in the
        form of the great lingam at Vishwanath. Shiva, pleased with Bhairav's penance and his
        act of righteousness in punishing Brahma's ego, declared:
      </p>
      <br>
      <blockquote style="border-left:2px solid var(--gold); padding-left:1.5rem; color:var(--gold-pale); font-style:italic;">
        "You shall be the eternal Kotwal of this city. No soul shall enter or depart Kashi
        without your knowledge. You shall be my representative here. Those who worship you
        with devotion shall be freed from all fears and the cycle of rebirth."
      </blockquote>
      <br>
      <p>
        From that day, Kaal Bhairav took up his residence at Kashi. He roams the cremation ghats —
        particularly <strong class="text-gold">Manikarnika Ghat</strong> — with his dog. He is the
        one who whispers the <em>Tarak Mantra</em> into the ear of every dying person in Kashi,
        granting them instant liberation regardless of their past karma.
      </p>
      <br>
      <p>
        His <em>danda</em> (staff) is the symbol of his authority. He carries a staff of justice,
        a noose to bind the wicked, and the kapala to feed the departed souls. He is simultaneously
        <em>Dandapani</em> (bearer of the staff of punishment) and the most compassionate liberator
        for souls who surrender to him.
      </p>
    </div>

