    <div class="story-block">
      <span class="source-tag">Source: Uttara Kalamrita; Kashi Kotwal oral tradition</span>
      <h3 class="story-title">The Sacred Dog — Bhairav's Vahana</h3>
      <p>
        There is a story from Kashi's oral tradition about why the dog is Bhairav's vehicle.
        Once, Brahmin priests in Kashi grew arrogant about their caste status and refused to
        feed a starving dog near the temple. The dog, near death from hunger, cried out.
      </p>
      <br>
      <p>
        That night, Lord Shiva appeared in the dream of the head priest in his Bhairav form
        and said: <em>"That dog is my body. You have starved me. The dog who appears to be
        a stray — I reside within it. Those who feed my dog, feed me. Those who torment it,
        torment me."</em>
      </p>
      <br>
      <p>
        From that time forward, it became a sacred practice in Kashi to feed dogs — especially
        black dogs — as an act of Bhairav seva. The tradition continues to this day.
        A <em>dog's yelp at night</em> is considered a sign of Bhairav's presence, a reminder
        that the divine walks the streets of Kashi in many forms.
      </p>
    </div>

