      <span class="source-tag">Source: Linga Purana; Tantric tradition</span>
      <h3 class="story-title">Kaal Bhairav and the Demon Durgama</h3>
      <p>
        The demon Durgama performed severe austerities and obtained from Brahma the boon that
        no god, man, or creature could kill him — except a child (<em>bala</em>) or an old person
        (<em>vriddha</em>). With this boon, Durgama spread terror across the three worlds, driving
        the gods from their abodes and plunging the universe into chaos.
      </p>
      <br>
      <p>
        The gods approached Lord Shiva for help. Shiva manifested Bhairav in the form of
        <strong class="text-gold">Batuk Bhairav</strong> — a young boy, eternally 16 years old.
        This childlike form of Bhairav approached Durgama playfully, as if unaware of any threat.
        The demon laughed at the child and attacked — only to be slain by Batuk Bhairav's
        trident in an instant.
      </p>
      <br>
      <p>
        This story teaches that the most fearsome power can reside in the simplest, most
        unassuming form. Batuk Bhairav is worshipped for protection, especially of children
        and households, and his mantra is considered extraordinarily powerful for removing
        obstacles and enemies.
      </p>
    </div>

