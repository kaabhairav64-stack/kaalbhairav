      <span class="source-tag">Source: Shiva Purana — Shatarudra Samhita; Skanda Purana — Kashi Khanda</span>
      <h3 class="story-title">The Birth of Kaal Bhairav — The Beheading of Brahma</h3>
      <p>
        In the primordial age, a great argument erupted between Lord Brahma (the Creator) and
        Lord Vishnu (the Preserver) over who was supreme. Both claimed to be the ultimate reality.
        At that moment, a blazing pillar of fire — infinite, without beginning or end — manifested
        between them. This was Lord Shiva as the <em>Jyotirlinga</em>, the cosmic light-pillar.
      </p>
      <br>
      <p>
        Brahma and Vishnu agreed: whoever finds the end of this pillar is supreme. Vishnu took the
        form of Varaha (boar) and dug downward. Brahma took the form of a swan and flew upward.
        Vishnu searched for thousands of years and could not find the bottom. Humbled, he returned
        and accepted Shiva's supremacy.
      </p>
      <br>
      <p>
        Brahma, however, was overtaken by his five heads' combined ego. On his way upward, he passed
        a <em>ketaki</em> flower drifting down. He asked the flower where it came from. The flower said
        it had been drifting for thousands of years from the top of the pillar. Brahma, desperate,
        made an agreement with the flower to lie for him — to claim that Brahma had reached the top.
      </p>
      <br>
      <p>
        When Brahma returned and falsely proclaimed his victory before Shiva, Shiva — who is
        <em>omniscient</em> — was filled with cosmic fury at this <em>brahmanicide of truth</em>.
        His anger took form as a being of terrifying aspect: <strong class="text-gold">Kaal Bhairav</strong>.
      </p>
      <br>
      <p>
        Kaal Bhairav manifested from Shiva's third eye (or from his left eyebrow, in some versions)
        — dark as a storm cloud, with blazing eyes, wearing a garland of skulls, holding the trident
        and a kapala (skull bowl). In one swift motion, he severed Brahma's fifth head — the one
        that had spoken the lie, the one representing false ego and pride. Brahma was left with four
        heads, humbled before Shiva.
      </p>
      <br>
      <p>
        But even as a manifestation of Shiva, Bhairav had committed <em>brahmahatya</em> — the sin
        of harming a Brahmin (Brahma is the ultimate Brahmin). The severed skull of Brahma stuck
        to Bhairav's hand and could not be removed. This is why Bhairav carries the <em>kapala</em>
        — the skull bowl. He became a wandering ascetic, a Kapalika, bearing the mark of his deed.
      </p>
      <br>
      <p>
        He wandered across all the sacred tirthas of the universe, carrying the skull. Wherever he went,
        the skull clung to him. Finally, he arrived at <strong class="text-gold">Kashi (Varanasi)</strong>.
        The moment his foot touched Kashi's soil, the skull fell from his hand. The great sin of
        brahmahatya was absolved. This is why Kashi is considered the ultimate liberator of all sins —
        even the sin of Kaal Bhairav was washed away here.
      </p>
      <br>
      <p>
        Moved by the holiness of Kashi, and on the request of Lord Shiva, Bhairav remained in Kashi
        forever as its <em>Kotwal</em> — the divine guardian and administrator of the eternal city.
      </p>
    </div>

