    <div class="story-block">
      <span class="source-tag">Source: Siddha Tantra tradition; Abhinavagupta's Tantraloka</span>
      <h3 class="story-title">Bhairav and the Kapalika Tradition</h3>
      <p>
        After the beheading of Brahma, Bhairav wandered as the first <em>Kapalika</em> ascetic —
        carrying the skull, bearing the mark of the ultimate sin, living outside all social norms.
        He ate from the skull, used cremation grounds as his home, wore skulls as ornaments.
      </p>
      <br>
      <p>
        The Kapalika tradition — one of the earliest Tantric sects — took Bhairav as their
        founding deity, modeling themselves on his wandering asceticism. They practiced in
        cremation grounds, used skull-bowls, and followed left-hand Tantric (<em>vamachara</em>)
        paths.
      </p>
      <br>
      <p>
        The deeper symbolism is profound: Bhairav carries the skull of Brahma to show that
        even <em>the greatest ego in creation</em> becomes an empty vessel — a mere bowl for
        Bhairav to feed from. The skull represents the dissolution of individual identity
        into cosmic consciousness. This is the core teaching of Kaal Bhairav: <em>ego is the
        only real sin, and time destroys all ego.</em>
      </p>
