        From that day, Kaal Bhairav took up his residence at Kashi. He roams the cremation ghats —
        particularly <strong class="text-gold">Manikarnika Ghat</strong> — with his dog. He is the
        one who whispers the <em>Tarak Mantra</em> into the ear of every dying person in Kashi,
        granting them instant liberation regardless of their past karma.<br><br>
        His <em>danda</em> (staff) is the symbol of his authority. He is simultaneously
        <em>Dandapani</em> (bearer of the staff of punishment) and the most compassionate liberator
